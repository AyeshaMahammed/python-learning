for num in range(0,11,2):
    print(num)
# This code prints even numbers from 0 to 10 using a for loop.
# The range function generates numbers from 0 to 10 with a step of 2, which results in even numbers.
# The for loop iterates through these numbers and prints each one.
# The output will be:
# 0
# 2
# 4
# 6
# 8
# 10